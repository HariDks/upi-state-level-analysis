sup { font-size: 0.75em; }

/* Title block (h1.title generated by pandoc YAML metadata) */
header.title-block-header h1.title {
  font-size: 1.8em;
  text-align: center;
  margin-top: 0.5em;
  margin-bottom: 0.3em;
}
header.title-block-header p.subtitle,
header.title-block-header p.author,

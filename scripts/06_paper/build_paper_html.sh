  text-align: center;
  margin: 0.2em 0;
  font-style: italic;
}
CSS

echo ""
echo "Wrote $HTML_OUT"
echo "Wrote $OUT_DIR/paper.css"
echo "Wrote $COMBINED_MD"
echo ""
echo "Open in browser:  open $HTML_OUT"
